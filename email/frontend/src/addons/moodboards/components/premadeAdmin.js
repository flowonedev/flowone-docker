import { C } from './templatePalette'

// ── Helpers ──

const n = (title, obj) => ({ ...obj, title })

const h = (text, x, y, w, fs = 14, color = C.textPrimary, weight = '700', align = 'left') => ({
  type: 'text', pos_x: x, pos_y: y, width: w, height: Math.round(fs * 2),
  content: text, style_data: { font_size: fs, font_weight: weight, font_family: 'Inter', text_color: color, text_align: align },
})

const t = (text, x, y, w, ht = 22, fs = 13, color = C.textSecondary, align = 'left') => ({
  type: 'text', pos_x: x, pos_y: y, width: w, height: ht,
  content: text, style_data: { font_size: fs, font_weight: '400', font_family: 'Inter', text_color: color, text_align: align, line_height: 1.5 },
})

const box = (x, y, w, ht, fill = C.bgCard, radius = 12, border = C.borderSubtle) => ({
  type: 'shape', pos_x: x, pos_y: y, width: w, height: ht,
  style_data: { shape_fill: fill, shape_opacity: 100, shape_border_width: 1, shape_border_color: border, shape_border_radius: radius },
})

const bg = (x, y, w, ht, fill = C.bgDark, radius = 0) => ({
  type: 'shape', pos_x: x, pos_y: y, width: w, height: ht,
  style_data: { shape_fill: fill, shape_opacity: 100, shape_border_width: 0, shape_border_color: fill, shape_border_radius: radius },
})

const btn = (text, x, y, w = 120, ht = 36, fill = C.primary, color = C.white) => ({
  type: 'shape', pos_x: x, pos_y: y, width: w, height: ht, content: text,
  style_data: {
    shape_fill: fill, shape_opacity: 100, shape_border_width: 0, shape_border_color: fill,
    shape_border_radius: 9999, shape_font_size: 13, shape_font_weight: '600', shape_font_color: color, shape_text_align: 'center',
  },
})

const btnGhost = (text, x, y, w = 120, ht = 36, border = C.primary, color = C.primary) => ({
  type: 'shape', pos_x: x, pos_y: y, width: w, height: ht, content: text,
  style_data: {
    shape_fill: 'transparent', shape_opacity: 100, shape_border_width: 2, shape_border_color: border,
    shape_border_radius: 9999, shape_font_size: 13, shape_font_weight: '600', shape_font_color: color, shape_text_align: 'center',
  },
})

const btnSoft = (text, x, y, w = 120, ht = 36, fill = C.primarySoft, color = C.primaryLight) => ({
  type: 'shape', pos_x: x, pos_y: y, width: w, height: ht, content: text,
  style_data: {
    shape_fill: fill, shape_opacity: 100, shape_border_width: 0, shape_border_color: fill,
    shape_border_radius: 9999, shape_font_size: 13, shape_font_weight: '600', shape_font_color: color, shape_text_align: 'center',
  },
})

const btnRect = (text, x, y, w = 120, ht = 36, fill = C.primary, color = C.white, radius = 8) => ({
  type: 'shape', pos_x: x, pos_y: y, width: w, height: ht, content: text,
  style_data: {
    shape_fill: fill, shape_opacity: 100, shape_border_width: 0, shape_border_color: fill,
    shape_border_radius: radius, shape_font_size: 13, shape_font_weight: '600', shape_font_color: color, shape_text_align: 'center',
  },
})

const chip = (text, x, y, w = 70, fill = C.primarySoft, color = C.primaryLight) => ({
  type: 'shape', pos_x: x, pos_y: y, width: w, height: 24, content: text,
  style_data: {
    shape_fill: fill, shape_opacity: 100, shape_border_width: 0, shape_border_color: fill,
    shape_border_radius: 9999, shape_font_size: 10, shape_font_weight: '600', shape_font_color: color, shape_text_align: 'center',
  },
})

const dot = (x, y, size = 8, fill = C.success) => bg(x, y, size, size, fill, 9999)
const line = (x, y, w) => bg(x, y, w, 1, C.borderSubtle)

